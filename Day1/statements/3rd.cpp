//switch statements:
#include<iostream>
using namespace std;
int main (){
    int month = 8;
    switch(month) {
        case 1:
        cout<<"january"<<endl;
        break;
        case 2 :
         cout<<"february"<<endl;
         break;
        case 3 :
         cout<<"march"<<endl;
         break;
        case 4:
         cout<<"april"<<endl;
         break;
         case 5 :
          cout<<"may"<<endl;
          break;
          case 6 :
          cout<<"june"<<endl;
          break;
          case 7 :
          cout<<"july"<<endl;
          break;
            default :
          cout << " out of the value "<<endl;
          
    }
  
    
}